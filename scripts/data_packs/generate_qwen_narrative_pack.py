# ─── Value-only generators (NO prefix tokens; v2 OPF convention) ──

def gen_amka(rng: random.Random) -> str:
    return "".join(rng.choices(string.digits, k=11))


def gen_afm(rng: random.Random) -> str:
    return "".join(rng.choices(string.digits, k=9))


def gen_adt(rng: random.Random) -> str:
    greek_letters = "ΑΒΓΔΕΖΗΘΙΚΛΜΝΞΟΠΡΣΤΥΦΧΨΩ"
    letters = "".join(rng.choices(greek_letters, k=rng.choice([2, 3])))
    digits = "".join(rng.choices(string.digits, k=6))
    sep = rng.choice(["-", " ", ""])
    return f"{letters}{sep}{digits}"


def gen_ama(rng: random.Random) -> str:
    return "".join(rng.choices(string.digits, k=rng.choice([7, 9])))


def gen_dl(rng: random.Random) -> str:
    return "".join(rng.choices(string.digits, k=9))


def gen_gemi(rng: random.Random) -> str:
    return "".join(rng.choices(string.digits, k=12))


# Weighted picker — 3x for hardest classes, 1x others
PII_GENERATORS_WEIGHTED: list[tuple[str, callable, float]] = [
    ("private_person", gen_person, 4.0),
    ("amka",           gen_amka,   3.0),
    ("ama",            gen_ama,    3.0),
    ("afm",            gen_afm,    2.5),
    ("adt",            gen_adt,    2.0),
    ("driver_license", gen_dl,     1.5),
    ("private_phone",  gen_phone,  1.5),
    ("private_email",  gen_email,  1.0),
    ("private_address",gen_address,1.0),
    ("private_date",   gen_date,   1.0),
    ("gemi",           gen_gemi,   1.0),
]

PII_LABELS_DESC = {
    "private_person":  "ονοματεπώνυμο",
    "amka":            "ΑΜΚΑ",
    "ama":             "ΑΜΑ ΙΚΑ",
    "afm":             "ΑΦΜ",
    "adt":             "ΑΔΤ",
    "driver_license":  "δίπλωμα οδήγησης",
    "private_phone":   "τηλέφωνο",
    "private_email":   "email",
    "private_address": "διεύθυνση",
    "private_date":    "ημερομηνία",
    "gemi":            "ΓΕΜΗ",
}


def _pick_pii_set(rng: random.Random, k: int) -> list[tuple[str, str]]:
    cats = [c for c, _, _ in PII_GENERATORS_WEIGHTED]
    weights = [w for _, _, w in PII_GENERATORS_WEIGHTED]
    chosen: list[str] = []
    available = list(zip(cats, weights))
    while len(chosen) < k and available:
        total = sum(w for _, w in available)
        r = rng.uniform(0, total)
        upto = 0.0
        for i, (c, w) in enumerate(available):
            upto += w
            if upto >= r:
                chosen.append(c)
                available.pop(i)
                break
    pairs = []
    for c in chosen:
        gen = next(g for n, g, _ in PII_GENERATORS_WEIGHTED if n == c)
        pairs.append((c, gen(rng)))
    return pairs


